                        <h2>about us</h2>
                        <p  style=" font-size:17px; visibility:hidden;"></p>
                        <p style=" font-size:17px; padding-bottom:15px;">  Disturbed by the knowledge that in 'the land of plenty' on any given night in Australia there are at least 105,000 homeless people across Australia, (Source: Australian Bureau of Statistics last census figures 2011); the people behind MyBowerhouse Foundation were motivated to make a difference.</p>
                        <p  style=" font-size:17px; padding-bottom:15px;">A figure of more than 100,000 Australians translates to the reality that right now one in every 200 Australians is homeless.  One in TWO hundred!
                       </p> 
                       <p style=" font-size:17px; padding-bottom:15px;">Specifically, women who are the hidden number, are our biggest focus.  Not only do they not appear on the streets at night even though they are homeless, they face exposure to elements that are challenging and dangerous.</p>
                    </div><!--END CLASS desc-->    
                    </div>
                </div><!--END ROW-->
                <p style=" font-size:17px; padding-bottom:15px;">Apart from this, they run the risk of the loss of their children to the State if they are found.  It's no wonder we don't encounter them as a large number on the streets! In saying that, they still exist…  We have a focus to help them more specifically as MyBowerhouse develops its strength.</p>
                <p style=" font-size:17px; padding-bottom:15px;">Equally disturbing was the realisation of how little of the funds donated to many charities and organisations actually reaches the "coal face" of those who need help most.</p>
                <p style=" font-size:17px; padding-bottom:15px;">The MyBowerhouse team, wanted to do things differently.   The board of directors was formed by individuals who wanted to do their 'bit' and to ensure that their efforts actually impacted on the end target – homeless and disadvantaged Australians, specifically but not limited to women.</p>
                <p style=" font-size:17px; padding-bottom:15px;">Rather than simply raising funds and funnelling them into an organisation that dissipates before actually doing the front line work, they decided that what they would purchase products in the first instance, "physical" things,  that could be donated and given directly to those who needed it.</p>
                <p style=" font-size:17px; padding-bottom:15px;">As they say, charity begins at home, and MyBowerhouse's philanthropy remains within Australia helping Australians, they deserve it.</p>        
